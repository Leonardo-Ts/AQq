package com.aaq.col.clases.webservices.movil;

public class GETMovilServicioGrua {
	private String usuario;

	private String passwd;

	private String atencion;

	private String codigoDeGrua;

	private String reporte;

	public String getUsuario() {
		return usuario;
	}

	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}

	public String getPasswd() {
		return passwd;
	}

	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}

	public String getAtencion() {
		return atencion;
	}

	public void setAtencion(String atencion) {
		this.atencion = atencion;
	}

	public String getCodigoDeGrua() {
		return codigoDeGrua;
	}

	public void setCodigoDeGrua(String codigoDeGrua) {
		this.codigoDeGrua = codigoDeGrua;
	}

	public String getReporte() {
		return reporte;
	}

	public void setReporte(String reporte) {
		this.reporte = reporte;
	}

	

}
