package com.aaq.col.clases.pojo.conclusion;

public class InformacionAbogado {
	
	private String nombreAbogado;
	private String claveAbogado;
	private String fechaAsignacion;
	private String nombreCoordJuridico;
	private String telCoordJuridico;
	
	public String getNombreAbogado() {
		return nombreAbogado;
	}
	public void setNombreAbogado(String nombreAbogado) {
		this.nombreAbogado = nombreAbogado;
	}
	public String getClaveAbogado() {
		return claveAbogado;
	}
	public void setClaveAbogado(String claveAbogado) {
		this.claveAbogado = claveAbogado;
	}
	public String getFechaAsignacion() {
		return fechaAsignacion;
	}
	public void setFechaAsignacion(String fechaAsignacion) {
		this.fechaAsignacion = fechaAsignacion;
	}
	public String getNombreCoordJuridico() {
		return nombreCoordJuridico;
	}
	public void setNombreCoordJuridico(String nombreCoordJuridico) {
		this.nombreCoordJuridico = nombreCoordJuridico;
	}
	public String getTelCoordJuridico() {
		return telCoordJuridico;
	}
	public void setTelCoordJuridico(String telCoordJuridico) {
		this.telCoordJuridico = telCoordJuridico;
	}
	
	

}
