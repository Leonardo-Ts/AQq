package com.aaq.col.clases.pojo.aaq;


public class InsertarCoordenadas {

	private String usuario;
	private String passwd;
	private String latitud;
	private String longitud;
	private String fechaLocalizacion;
	private String horaLocalizacion;
	private String fechaRecepcion;
	private String horaRecepcion;
	private String distanciaAlArribo;
	
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getPasswd() {
		return passwd;
	}
	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
	public String getLatitud() {
		return latitud;
	}
	public void setLatitud(String latitud) {
		this.latitud = latitud;
	}
	public String getLongitud() {
		return longitud;
	}
	public void setLongitud(String longitud) {
		this.longitud = longitud;
	}
	
	public String getDistanciaAlArribo() {
		return distanciaAlArribo;
	}
	public void setDistanciaAlArribo(String distanciaAlArribo) {
		this.distanciaAlArribo = distanciaAlArribo;
	}
	public String getFechaLocalizacion() {
		return fechaLocalizacion;
	}
	public void setFechaLocalizacion(String fechaLocalizacion) {
		this.fechaLocalizacion = fechaLocalizacion;
	}
	public String getHoraLocalizacion() {
		return horaLocalizacion;
	}
	public void setHoraLocalizacion(String horaLocalizacion) {
		this.horaLocalizacion = horaLocalizacion;
	}
	public String getFechaRecepcion() {
		return fechaRecepcion;
	}
	public void setFechaRecepcion(String fechaRecepcion) {
		this.fechaRecepcion = fechaRecepcion;
	}
	public String getHoraRecepcion() {
		return horaRecepcion;
	}
	public void setHoraRecepcion(String horaRecepcion) {
		this.horaRecepcion = horaRecepcion;
	}
	
	
	
}
