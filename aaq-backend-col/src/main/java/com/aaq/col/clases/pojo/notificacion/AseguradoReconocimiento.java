package com.aaq.col.clases.pojo.notificacion;


public class AseguradoReconocimiento {

	private String poliza;
	private String nombre;
	private int tipoAfectado;
	private String inciso;
	private String vehiculoDescripcion;
	private String vehiculoPlacas;
	private String vehiculoSerie;
	private String tipoIdentificacion;
	private String porcentajeSimilitud;
	
	
	public String getPoliza() {
		return poliza;
	}
	public void setPoliza(String poliza) {
		this.poliza = poliza;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getTipoAfectado() {
		return tipoAfectado;
	}
	public void setTipoAfectado(int tipoAfectado) {
		this.tipoAfectado = tipoAfectado;
	}
	
	public String getInciso() {
		return inciso;
	}
	public void setInciso(String inciso) {
		this.inciso = inciso;
	}
	public String getVehiculoDescripcion() {
		return vehiculoDescripcion;
	}
	public void setVehiculoDescripcion(String vehiculoDescripcion) {
		this.vehiculoDescripcion = vehiculoDescripcion;
	}
	public String getVehiculoPlacas() {
		return vehiculoPlacas;
	}
	public void setVehiculoPlacas(String vehiculoPlacas) {
		this.vehiculoPlacas = vehiculoPlacas;
	}
	public String getVehiculoSerie() {
		return vehiculoSerie;
	}
	public void setVehiculoSerie(String vehiculoSerie) {
		this.vehiculoSerie = vehiculoSerie;
	}
	public String getTipoIdentificacion() {
		return tipoIdentificacion;
	}
	public void setTipoIdentificacion(String tipoIdentificacion) {
		this.tipoIdentificacion = tipoIdentificacion;
	}
	public String getPorcentajeSimilitud() {
		return porcentajeSimilitud;
	}
	public void setPorcentajeSimilitud(String porcentajeSimilitud) {
		this.porcentajeSimilitud = porcentajeSimilitud;
	}
	
}
