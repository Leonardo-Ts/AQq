package com.aaq.col.clases.sac.model;

public class ConfirmarGruaSac {
	
	private String reporte;
	private String involucrado;
	private String proveedor;
	private String status;
	private String comentario;
	/**
	 * @return the reporte
	 */
	public String getReporte() {
		return reporte;
	}
	/**
	 * @param reporte the reporte to set
	 */
	public void setReporte(String reporte) {
		this.reporte = reporte;
	}
	/**
	 * @return the involucrado
	 */
	public String getInvolucrado() {
		return involucrado;
	}
	/**
	 * @param involucrado the involucrado to set
	 */
	public void setInvolucrado(String involucrado) {
		this.involucrado = involucrado;
	}
	/**
	 * @return the proveedor
	 */
	public String getProveedor() {
		return proveedor;
	}
	/**
	 * @param proveedor the proveedor to set
	 */
	public void setProveedor(String proveedor) {
		this.proveedor = proveedor;
	}
	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}
	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}
	/**
	 * @return the comentarios
	 */
	public String getComentario() {
		return comentario;
	}
	/**
	 * @param comentarios the comentarios to set
	 */
	public void setComentarios(String comentarios) {
		this.comentario = comentarios;
	}
	
	

}
