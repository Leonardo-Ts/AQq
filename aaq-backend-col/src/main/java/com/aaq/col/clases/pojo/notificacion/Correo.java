package com.aaq.col.clases.pojo.notificacion;

public class Correo {
	
	private String usuario;
	private String pwd;
	private String reporte;
	private String claveAccidente;
	private String equipoPesado;
	
	
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getReporte() {
		return reporte;
	}
	public void setReporte(String reporte) {
		this.reporte = reporte;
	}
	public String getClaveAccidente() {
		return claveAccidente;
	}
	public void setClaveAccidente(String claveAccidente) {
		this.claveAccidente = claveAccidente;
	}
	public String getEquipoPesado() {
		return equipoPesado;
	}
	public void setEquipoPesado(String equipoPesado) {
		this.equipoPesado = equipoPesado;
	}
	
	

}
