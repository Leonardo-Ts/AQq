package com.aaq.col.clases.webservices.movil;

public class GETMovilDatosAsegurado {
	private String edad;
	private String sexo;
	private String aseguradoNombre;
	private String aseguradoCorreo;
	private String aseguradoTelefono;
	
	public GETMovilDatosAsegurado() {
		super();
	}
	
	public String getEdad() {
		return edad;
	}
	public void setEdad(String edad) {
		this.edad = edad;
	}
	public String getSexo() {
		return sexo;
	}
	public void setSexo(String sexo) {
		this.sexo = sexo;
	}
	public String getAseguradoNombre() {
		return aseguradoNombre;
	}
	public void setAseguradoNombre(String aseguradoNombre) {
		this.aseguradoNombre = aseguradoNombre;
	}
	public String getAseguradoCorreo() {
		return aseguradoCorreo;
	}
	public void setAseguradoCorreo(String aseguradoCorreo) {
		this.aseguradoCorreo = aseguradoCorreo;
	}
	public String getAseguradoTelefono() {
		return aseguradoTelefono;
	}
	public void setAseguradoTelefono(String aseguradoTelefono) {
		this.aseguradoTelefono = aseguradoTelefono;
	}

	

}
