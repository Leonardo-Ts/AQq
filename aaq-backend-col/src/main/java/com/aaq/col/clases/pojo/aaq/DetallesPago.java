package com.aaq.col.clases.pojo.aaq;

public class DetallesPago {

	private String referenciaDeCobro;
	private String status;
	private String tipoDeTarjeta;
	private String montoCobrado;
	private String fechaPago;
	private String horaPago;
	private String noAutorizacion;
	private String folioTransaccion;
	
	
	public String getReferenciaDeCobro() {
		return referenciaDeCobro;
	}
	public void setReferenciaDeCobro(String referenciaDeCobro) {
		this.referenciaDeCobro = referenciaDeCobro;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getTipoDeTarjeta() {
		return tipoDeTarjeta;
	}
	public void setTipoDeTarjeta(String tipoDeTarjeta) {
		this.tipoDeTarjeta = tipoDeTarjeta;
	}
	public String getMontoCobrado() {
		return montoCobrado;
	}
	public void setMontoCobrado(String montoCobrado) {
		this.montoCobrado = montoCobrado;
	}
	public String getFechaPago() {
		return fechaPago;
	}
	public void setFechaPago(String fechaPago) {
		this.fechaPago = fechaPago;
	}
	public String getHoraPago() {
		return horaPago;
	}
	public void setHoraPago(String horaPago) {
		this.horaPago = horaPago;
	}
	public String getNoAutorizacion() {
		return noAutorizacion;
	}
	public void setNoAutorizacion(String noAutorizacion) {
		this.noAutorizacion = noAutorizacion;
	}
	public String getFolioTransaccion() {
		return folioTransaccion;
	}
	public void setFolioTransaccion(String folioTransaccion) {
		this.folioTransaccion = folioTransaccion;
	}
	
	
	
}
