package com.aaq.col.clases.webservices.movil;

public class GETMovilServicioCapufe {
	private String usuario;
	private String passwd;
	private String numeroKilometro;
	private boolean danosCam;
	private String cuerpo;
	private String responsable;
	private String causaAccidente;
	private String comoOcurrio;

	public GETMovilServicioCapufe() {
		super();
	}

	public String getUsuario() {
		return usuario;
	}

	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}

	public String getPasswd() {
		return passwd;
	}

	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}

	public String getNumeroKilometro() {
		return numeroKilometro;
	}

	public void setNumeroKilometro(String numeroKilometro) {
		this.numeroKilometro = numeroKilometro;
	}

	public boolean isDanosCam() {
		return danosCam;
	}

	public void setDanosCam(boolean danosCam) {
		this.danosCam = danosCam;
	}

	public String getCuerpo() {
		return cuerpo;
	}

	public void setCuerpo(String cuerpo) {
		this.cuerpo = cuerpo;
	}

	public String getResponsable() {
		return responsable;
	}

	public void setResponsable(String responsable) {
		this.responsable = responsable;
	}

	public String getCausaAccidente() {
		return causaAccidente;
	}

	public void setCausaAccidente(String causaAccidente) {
		this.causaAccidente = causaAccidente;
	}

	public String getComoOcurrio() {
		return comoOcurrio;
	}

	public void setComoOcurrio(String comoOcurrio) {
		this.comoOcurrio = comoOcurrio;
	}

	
}
