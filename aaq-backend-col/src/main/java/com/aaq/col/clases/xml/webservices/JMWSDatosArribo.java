package com.aaq.col.clases.xml.webservices;

public class JMWSDatosArribo {

	private String FechaArribo;
	private String HoraArribo;
	private String Reporte;
	private String Placas;
	private String Serie;
	private String Sistema;
	private String TramoCarretero;
	private String Longitud;
	private String Latitud;
	private String QuienLlegoPrimero;
	
	public JMWSDatosArribo() {
		super();
	}
	
	public String getFechaArribo() {
		return FechaArribo;
	}
	public void setFechaArribo(String fechaArribo) {
		FechaArribo = fechaArribo;
	}
	public String getHoraArribo() {
		return HoraArribo;
	}
	public void setHoraArribo(String horaArribo) {
		HoraArribo = horaArribo;
	}
	public String getReporte() {
		return Reporte;
	}
	public void setReporte(String reporte) {
		Reporte = reporte;
	}
	public String getPlacas() {
		return Placas;
	}
	public void setPlacas(String placas) {
		Placas = placas;
	}
	public String getSerie() {
		return Serie;
	}
	public void setSerie(String serie) {
		Serie = serie;
	}
	public String getSistema() {
		return Sistema;
	}
	public void setSistema(String sistema) {
		Sistema = sistema;
	}
	public String getTramoCarretero() {
		return TramoCarretero;
	}
	public void setTramoCarretero(String tramoCarretero) {
		TramoCarretero = tramoCarretero;
	}
	public String getLongitud() {
		return Longitud;
	}
	public void setLongitud(String longitud) {
		Longitud = longitud;
	}
	public String getLatitud() {
		return Latitud;
	}
	public void setLatitud(String latitud) {
		Latitud = latitud;
	}
	public String getQuienLlegoPrimero() {
		return QuienLlegoPrimero;
	}
	public void setQuienLlegoPrimero(String quienLlegoPrimero) {
		QuienLlegoPrimero = quienLlegoPrimero;
	}
	
	
}
