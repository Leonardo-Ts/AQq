package com.aaq.col.clases.xml.webservices;

public class Resultado {

	private String CodigoResultado;
	private String DescResultado;
	private String fecha;
	
	public String getCodigoResultado() {
		return CodigoResultado;
	}
	public void setCodigoResultado(String codigoResultado) {
		CodigoResultado = codigoResultado;
	}
	public String getDescResultado() {
		return DescResultado;
	}
	public void setDescResultado(String descResultado) {
		DescResultado = descResultado;
	}
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	
	
}
