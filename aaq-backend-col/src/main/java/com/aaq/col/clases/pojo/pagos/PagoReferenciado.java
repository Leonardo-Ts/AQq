package com.aaq.col.clases.pojo.pagos;

public class PagoReferenciado {

	private String usuario;
	private String pwd;
	private String numReporte;
	private String tipoDeCobro;
	private String coberturaAfectada;
	private String monto;
	private String correoElectronico;
	private String celular;
	private String referencia;
	private boolean convenioTodos;
	private String nombreBanco;
	
	
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getNumReporte() {
		return numReporte;
	}
	public void setNumReporte(String numReporte) {
		this.numReporte = numReporte;
	}
	public String getTipoDeCobro() {
		return tipoDeCobro;
	}
	public void setTipoDeCobro(String tipoDeCobro) {
		this.tipoDeCobro = tipoDeCobro;
	}
	public String getCoberturaAfectada() {
		return coberturaAfectada;
	}
	public void setCoberturaAfectada(String coberturaAfectada) {
		this.coberturaAfectada = coberturaAfectada;
	}
	public String getMonto() {
		return monto;
	}
	public void setMonto(String monto) {
		this.monto = monto;
	}
	public String getCorreoElectronico() {
		return correoElectronico;
	}
	public void setCorreoElectronico(String correoElectronico) {
		this.correoElectronico = correoElectronico;
	}
	public String getCelular() {
		return celular;
	}
	public void setCelular(String celular) {
		this.celular = celular;
	}
	public String getReferencia() {
		return referencia;
	}
	public void setReferencia(String referencia) {
		this.referencia = referencia;
	}
	public String getNombreBanco() {
		return nombreBanco;
	}
	public void setNombreBanco(String nombreBanco) {
		this.nombreBanco = nombreBanco;
	}
	public boolean isConvenioTodos() {
		return convenioTodos;
	}
	public void setConvenioTodos(boolean convenioTodos) {
		this.convenioTodos = convenioTodos;
	}
	
	
	
}
