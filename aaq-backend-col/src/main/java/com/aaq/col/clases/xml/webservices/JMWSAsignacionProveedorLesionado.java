public class JMWSAsignacionProveedorLesionado {

	private String nombreLesionado;
	private String sexoLesionado;
	private Integer edadLesionado;
	
	public JMWSAsignacionProveedorLesionado() {
		super();
	}

	public String getNombreLesionado() {
		return nombreLesionado;
	}

	public void setNombreLesionado(String nombreLesionado) {
		this.nombreLesionado = nombreLesionado;
	}

	public String getSexoLesionado() {
		return sexoLesionado;
	}

	public void setSexoLesionado(String sexoLesionado) {
		this.sexoLesionado = sexoLesionado;
	}

	public Integer getEdadLesionado() {
		return edadLesionado;
	}

	public void setEdadLesionado(Integer edadLesionado) {
		this.edadLesionado = edadLesionado;
	}

	
	
}
