package com.aaq.col.clases.xml.webservices;

public class JMWSAsignacionGrua {

	private String NumProveedor;
	private String Password;
	private String Ramo;
	private String Ejercicio;
	private String NumReporte;
	private String Atencion;
	private JMWSAsignacionGruaVehiculo Vehiculo;
	
	
	public String getNumProveedor() {
		return NumProveedor;
	}
	public void setNumProveedor(String numProveedor) {
		NumProveedor = numProveedor;
	}
	public String getPassword() {
		return Password;
	}
	public void setPassword(String password) {
		Password = password;
	}
	public String getRamo() {
		return Ramo;
	}
	public void setRamo(String ramo) {
		Ramo = ramo;
	}
	public String getEjercicio() {
		return Ejercicio;
	}
	public void setEjercicio(String ejercicio) {
		Ejercicio = ejercicio;
	}
	public String getNumReporte() {
		return NumReporte;
	}
	public void setNumReporte(String numReporte) {
		NumReporte = numReporte;
	}
	public String getAtencion() {
		return Atencion;
	}
	public void setAtencion(String atencion) {
		Atencion = atencion;
	}
	public JMWSAsignacionGruaVehiculo getVehiculo() {
		return Vehiculo;
	}
	public void setVehiculo(JMWSAsignacionGruaVehiculo vehiculo) {
		Vehiculo = vehiculo;
	}
	
	
	
}
