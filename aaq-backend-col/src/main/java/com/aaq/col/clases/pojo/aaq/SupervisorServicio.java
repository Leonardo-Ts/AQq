package com.aaq.col.clases.pojo.aaq;

public class SupervisorServicio {
	
	private String tipoServicio;
	private String nombreProveedor;
	private String folio;
	private String tipoAfectado;
	private String supervisorServicio;
	private String telefono;
	private String numReporte;
	
	
	public String getTipoServicio() {
		return tipoServicio;
	}
	public void setTipoServicio(String tipoServicio) {
		this.tipoServicio = tipoServicio;
	}
	public String getNombreProveedor() {
		return nombreProveedor;
	}
	public void setNombreProveedor(String nombreProveedor) {
		this.nombreProveedor = nombreProveedor;
	}
	public String getFolio() {
		return folio;
	}
	public void setFolio(String folio) {
		this.folio = folio;
	}
	public String getTipoAfectado() {
		return tipoAfectado;
	}
	public void setTipoAfectado(String tipoAfectado) {
		this.tipoAfectado = tipoAfectado;
	}
	public String getSupervisorServicio() {
		return supervisorServicio;
	}
	public void setSupervisorServicio(String supervisorServicio) {
		this.supervisorServicio = supervisorServicio;
	}
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	public String getNumReporte() {
		return numReporte;
	}
	public void setNumReporte(String numReporte) {
		this.numReporte = numReporte;
	}
	
	
	

}
