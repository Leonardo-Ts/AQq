package com.aaq.col.clases.pojo.conclusion;

public class DocEntregados {
	
	private String dua;
	private String admAutos;
	private String asigAbogado;
	private String encuesta;
	
	
	public String getDua() {
		return dua;
	}
	public void setDua(String dua) {
		this.dua = dua;
	}
	public String getAdmAutos() {
		return admAutos;
	}
	public void setAdmAutos(String admAutos) {
		this.admAutos = admAutos;
	}
	public String getAsigAbogado() {
		return asigAbogado;
	}
	public void setAsigAbogado(String asigAbogado) {
		this.asigAbogado = asigAbogado;
	}
	public String getEncuesta() {
		return encuesta;
	}
	public void setEncuesta(String encuesta) {
		this.encuesta = encuesta;
	}
	
	
	

}
