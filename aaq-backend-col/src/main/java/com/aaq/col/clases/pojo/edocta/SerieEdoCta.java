package com.aaq.col.clases.pojo.edocta;

public class SerieEdoCta {

	private String poliza;
	private String endoso;
	private String inciso;
	private String codAgente;
	private String inicioVigencia;
	private String terminoVigencia;
	private String ramo;
	private String numSiniestro;
	private String fechaSiniestro;
	
	
	public String getPoliza() {
		return poliza;
	}
	public void setPoliza(String poliza) {
		this.poliza = poliza;
	}
	public String getEndoso() {
		return endoso;
	}
	public void setEndoso(String endoso) {
		this.endoso = endoso;
	}
	public String getInciso() {
		return inciso;
	}
	public void setInciso(String inciso) {
		this.inciso = inciso;
	}
	public String getCodAgente() {
		return codAgente;
	}
	public void setCodAgente(String codAgente) {
		this.codAgente = codAgente;
	}
	public String getInicioVigencia() {
		return inicioVigencia;
	}
	public void setInicioVigencia(String inicioVigencia) {
		this.inicioVigencia = inicioVigencia;
	}
	public String getTerminoVigencia() {
		return terminoVigencia;
	}
	public void setTerminoVigencia(String terminoVigencia) {
		this.terminoVigencia = terminoVigencia;
	}
	public String getRamo() {
		return ramo;
	}
	public void setRamo(String ramo) {
		this.ramo = ramo;
	}
	public String getNumSiniestro() {
		return numSiniestro;
	}
	public void setNumSiniestro(String numSiniestro) {
		this.numSiniestro = numSiniestro;
	}
	public String getFechaSiniestro() {
		return fechaSiniestro;
	}
	public void setFechaSiniestro(String fechaSiniestro) {
		this.fechaSiniestro = fechaSiniestro;
	}
	
	
}
