package com.aaq.col.clases.pojo.edocta;

public class CoberturasEdoCta {

	private String clave;
	private String nombre;
	private String sumAseg;
	private String deducible;
	private String importeDedu;
	
	
	public String getClave() {
		return clave;
	}
	public void setClave(String clave) {
		this.clave = clave;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getSumAseg() {
		return sumAseg;
	}
	public void setSumAseg(String sumAseg) {
		this.sumAseg = sumAseg;
	}
	public String getDeducible() {
		return deducible;
	}
	public void setDeducible(String deducible) {
		this.deducible = deducible;
	}
	public String getImporteDedu() {
		return importeDedu;
	}
	public void setImporteDedu(String importeDedu) {
		this.importeDedu = importeDedu;
	}

	
}
