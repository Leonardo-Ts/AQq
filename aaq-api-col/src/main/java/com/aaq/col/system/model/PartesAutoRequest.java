package com.aaq.col.system.model;

public class PartesAutoRequest {

	private String tipoPartes;
	private String numParte;
	private String nombreParte;
	
	public String getTipoPartes() {
		return tipoPartes;
	}
	public void setTipoPartes(String tipoPartes) {
		this.tipoPartes = tipoPartes;
	}
	public String getNumParte() {
		return numParte;
	}
	public void setNumParte(String numParte) {
		this.numParte = numParte;
	}
	public String getNombreParte() {
		return nombreParte;
	}
	public void setNombreParte(String nombreParte) {
		this.nombreParte = nombreParte;
	}
	
	
}
